// Existem 3 maneiras de declarar variaveis em Javascript: Var, Let e Const
// Var foi a forma original (primeiras versões) - Let e Const vieram depois
// Var e Let sao muito similares, enquanto const é constante

var pessoa;
pessoa = 'Fernando'
console.log(pessoa); //Fernando

let professora;
professora = 'Ana';
console.log(professora);

// Variáveis do tipo const são constantes - não podem ser alteradas
const estudante = 'Caroline';
